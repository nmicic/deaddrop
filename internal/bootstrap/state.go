var bootstrapSalt [16]byte

func init() {
	h := sha256.Sum256([]byte("deaddrop-bootstrap-v1"))
	copy(bootstrapSalt[:], h[:16])
}

func BootstrapSalt() [16]byte { return bootstrapSalt }

func DerivePassphraseKey(passphrase []byte) []byte {
	return argon2.IDKey(passphrase, bootstrapSalt[:], 3, 1<<17, 4, 32)
}

func GenerateIdentity() (sk, pk [32]byte, err error) {
	if _, err = rand.Read(sk[:]); err != nil {
		return sk, pk, err
	}
	curve25519.ScalarBaseMult(&pk, &sk)
	if IsLowOrderPoint(pk) {
		return sk, pk, &Leg12Error{Code: exitcode.CryptoLocal, Detail: "generated low-order pubkey"}
	}
	return sk, pk, nil
}

