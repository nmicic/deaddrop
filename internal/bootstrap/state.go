func IsLowOrderPoint(pk [32]byte) bool {
	var match int
	for i := range lowOrderPoints {
		match |= subtle.ConstantTimeCompare(pk[:], lowOrderPoints[i][:])
	}
	return match == 1
}

func LegKey(passphraseKey []byte, direction byte) ([]byte, error) {
	info := append([]byte("deaddrop-bootstrap-leg12"), direction)
	return crypto.DeriveKey(passphraseKey, nil, info, 32)
}

func LegSlotKey(passphraseKey []byte, direction byte) ([]byte, error) {
	info := append([]byte("deaddrop-bootstrap-slot"), direction)
	return crypto.DeriveKey(passphraseKey, nil, info, 32)
}

func LegSlotID(slotKey []byte, b uint64) ([]byte, error) {
	return slot.SlotID(slotKey, b, 0)
}

type Role int

const (
	Initiator Role = iota
	Responder
)

type State struct {
	IdentitySK    [32]byte
	IdentityPK    [32]byte
	PeerPK        [32]byte
	PassphraseKey []byte
	Role          Role

	// IdentityStore is the keyring backend FinishBootstrap writes
	// the per-pair identity entry into (D-65 work item). The
	// bootstrap CLI guarantees a non-nil Store: either New()
	// succeeded or Noop() was substituted on the ErrUnsupported
	// branch. Callers therefore do not need a nil-guard.
	IdentityStore identitystore.Store
}

// ZeroizeBootstrap clears the bootstrap-only secrets — the
// passphrase-derived key. Identity material (IdentitySK / IdentityPK
// / PeerPK) is intentionally NOT touched here: those bytes outlive
// the bootstrap leg work and are zeroized by ZeroizeIdentity (or by
// FinishBootstrap's own internal defer once the identity-store Put
// returns).
//
// CLI callers should `defer s.ZeroizeBootstrap()` immediately after
// State construction and rely on FinishBootstrap to zero the
// identity fields (D-65).
func (s *State) ZeroizeBootstrap() {
	zeroize(s.PassphraseKey)
}

// ZeroizeIdentity clears the identity-bearing fields. IdentityPK is
// included in the cleared set even though it is public; the original
// Zeroize asymmetry (IdentityPK left untouched) is fixed in passing.
//
// FinishBootstrap calls this from its own defer once the identity
// entry has been handed to the keyring; CLI callers do NOT need to
// invoke it directly.
func (s *State) ZeroizeIdentity() {
	zeroize(s.IdentitySK[:])
	zeroize(s.IdentityPK[:])
	zeroize(s.PeerPK[:])
}

// Zeroize is the deprecated combined alias kept so existing tests
// (state_test.go) continue to compile. New code MUST call
// ZeroizeBootstrap and ZeroizeIdentity at their respective lifecycle
// points (D-65 split).
func (s *State) Zeroize() {
	s.ZeroizeBootstrap()
	s.ZeroizeIdentity()
}

func zeroize(b []byte) {
	for i := range b {
		b[i] = 0
	}
}
